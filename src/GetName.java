public class GetName {
    public static void main(String[] args) {

        char ch = 'c';
        switch (ch) {
            case 'a':
                System.out.print("a");
                break;
            case 'b':
                System.out.print("ab");
            case 'c':
                System.out.print("c");
            default:
                System.out.print("d");

        }
    }
}